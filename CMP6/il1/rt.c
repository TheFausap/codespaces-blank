/* read tape */

char *readp(char *p)
{
    /* five holes */
    char *r;
    char c;
    int j=0;
    r=calloc(6,sizeof(r));
    while((c=*p++)!='\n') {
        if(c=='O') r[j]=1+'0';
        else if(c=='o') continue;
        else r[j]=0+'0';
        j++;
    }
    return r;
}

